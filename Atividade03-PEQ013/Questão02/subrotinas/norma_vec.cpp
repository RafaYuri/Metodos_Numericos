//===========================================================================
// Norma L2 de um vetor
//===========================================================================

double nl2 ( v1d vetor )
{
    double l2 = 0 ;
    for ( int i = 0 ; i < vetor.size () ; i++ )
    {
        l2 += pow ( vetor [ i ], 2 ) ;
    }
    l2 = sqrt ( l2 ) ;

    return l2 ;
}
